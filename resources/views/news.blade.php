                    <p class="jhxq_text" style="text-indent:2em;">
                        行业首创数据集成+应用集成 打通平台数据壁垒
                        一项颠覆以往的新技术必然给研发环节带来技术挑战，畅捷通为此付出了大量的努力。
                        小微企业云平台已经在硅谷秘密研发四五年之久，本次发布的工作圈采取了元数据集市的众帮模式，由平台上的所有ISV开发商共同贡献元数据，形成全局的元数据；通过平台级的业务消息机制，实现跨应用的业务逻辑集成，从而支持平台上不同ISV开发的应用，做到彼此之间的数据集成和业务逻辑集成。这种创新机制实现了“热插拔”式的数据集成和应用集成，不仅打破了数据壁垒，使数据孤岛的问题不复存在，而且多年以来困扰企业的业务流程不畅的困难也迎刃而解。
                        正是背后首屈一指的技术，打造了支撑企业应用深度集成的工作圈开放平台，目前也是全球唯一正式发布的此类商用技术平台。
                        畅捷通在企业级市场深耕多年，已经形成了完善的企业财务及管理应用体系，获得超过百万家小微企业的认可，并在全国范围内建立了成熟的线下服务体系。此次工作圈的发布，将成为畅捷通加速推动小微企业互联网化的契机，畅捷通将结合积累优势，快速推动工作圈在小微企业群体中的应用，助力小微企业健康成长！
