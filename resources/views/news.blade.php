                    <p class="jhxq_text" style="text-indent:2em;">9月21日，畅捷通在北京正式发布小微企业管理云平台——工作圈。在发布会上，作为国内首家实现第三方应用数据打通的云平台，工作圈与阿里邮箱、263云通信、易快报、企业头条、云账户、随锐科技等合作伙伴举行了签约仪式。未来工作圈将立足财务核心应用，集合生态伙伴一圈搞定小微企业的“人、财、货”，让小微企业的管理更敏捷。</p>
                    <p class="jhxq_pic"><img src="{{ asset('images/20160923094024_2m3hot1hva.jpg') }}"></p>
                    <p class="jhxq_text" style="text-indent:2em;">SaaS蓬勃背后潜藏数据短板
                        随着互联网、移动互联网的快速发展，以“云移物大智”为代表的新技术席卷社会各层，传统企业转型、创新创业成为近几年的关键词。小微企业作为中国企业数量最多的支柱群体，更是面临着巨大的发展压力和机遇。
                        为推动小微企业“互联网+”行动计划，促进转型升级并健康发展，云技术将在互联网新一代的信息技术与企业间的交易进行信息化整合，为企业提供快速灵活的信息分享与集成、沟通协作、及互联网化资源共享的信息化平台，也成为小微企业成长过程中的优先选择。
                        然而很多企业由于业务需求紧迫或者经验缺失，快速上线许多应用，但缺乏顶层设计，各应用系统间缺乏联动，随着业务需求上线更多应用，企业移动应用的碎片化以及应用的孤岛现象愈发严重。事实上，企业内的人力资源系统、ERP系统、财务系统、库存管理系统等看似各自独立，但所有系统在数据和业务逻辑上却都密切相关，而这些相互独立的应用之间数据互通和流程衔接目前还远远达不到企业的预期，也没有看到很好的解决方案。
                        这也导致企业使用了多少个应用，就存在多少个数据孤岛，当需要跨应用使用数据时，只能通过各个应用之间互相调用API接口来实现。随着接入的应用数量不断增长，ISV应用需要兼容的API接口数量会爆炸式增长。这种模式存在一个严重的问题。由于数据被ISV掌握，企业需要更换一个应用的迁移成本非常高昂，对用户方来说，软件时代被一个应用锁定的痛点依然没有解决。
                        因此，能够打通企业不同应用间数据的工作平台，为小微企业快速发展所急需。
                    </p>
