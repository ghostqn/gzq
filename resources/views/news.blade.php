                    <p class="jhxq_text" style="text-indent:2em;">
                        工作圈打通数据迸发管理价值
                        作为小微企业财务及管理服务领导厂商，畅捷通深知企业应用集成的迫切需求和技术挑战，为实现企业关键业务系统的深度数据集成和业务流程的无缝整合，在美国硅谷建立了研发中心并引入了一批在Salesforce、Google、Oracle等公司有丰富经验的资深技术专家，依托畅捷通多年来服务于中国百万级小微企业的经验，和对各行业用户需求的深入理解，打造了服务于小微企业的企业应用云平台——工作圈。
                        工作圈全面支撑平台内应用的深度数据集成，从而将散落在企业经营管理过程中的数据整合贯通，帮助企业实现全局数据地快速、无阻流动，驱动企业数据化经营，提升企业整体效益。过去，需要跨应用使用数据时，只能通过定制开发、调用API接口的方式；现在，在工作圈上，小微企业可以轻松获取多个数据共享的应用、数据协同的应用和数据集成的应用，无需再为数据孤岛所扰。
                        同时，基于平台和应用运转过程中产生的海量业务数据和行为数据，工作圈通过对数据的挖掘、分析，迸发数据潜藏价值，为小微企业提供更高附加值的数据服务，以及衍生的金融等增值服务，以此吸引更多以数据为核心的服务商的合作。
                        此外，作为小微企业管理云平台，工作圈连接小微企业、ISV和数据增值服务商三方，为小微企业提供数据贯通的一站式管理服务，全面满足其发展需求，同时企业可以在平台上灵活选择所需应用，真正做到自己的企业管理自己说了算，依托平台也不必担心数据同步的问题；对ISV开放协议标准、接口和典型应用的源码等优势，共建企业生态平台，实现多方共赢。

                        基于财务核心应用 打造小微企业管理新生态
                        事实上，工作圈小微企业管理云平台的目标是推动小微企业、ISV及数据增值服务商快速成长，在打通数据的基础上，大力推广平台应用的易用性，帮助ISV找用户，帮助企业随时随地按需应用，从而建立起一个更大产业生态圈。
                        畅捷通多年耕耘小微企业的财务服务基因，以及100多万付费企业用户和多年技术沉淀与投入，使得工作圈在企业服务类ISV中具有天然的向心力。作为小微企业管理云平台的工作圈以财务ERP为核心应用，打通应用间的数据壁垒，并提供一些终身免费的基础能力，打造小微企业管理新生态。
                        工作圈已经定义好标准的财税和进销存等小微企业必要的核心应用，并以开放的生态理念，聚合第三方合作伙伴及应用，包括CRM、HR、会员管理，以及具有相关行业属性的应用管理，都将会与平台展开合作，共同打造一站式的云服务平台，以此为小微企业提供低成本、高效率的服务。
